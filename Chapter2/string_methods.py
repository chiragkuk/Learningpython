name="chirag kukreja"

#1. len() function it count spaces and give the length of String
print(len(name))

#2. lower() method gives the whole string in lower case
print(name.lower())

#3. upper() method gives the whole string in Upper Case
print(name.upper())

#4. Title() method gives the string in title form i.e. "chirag kukreja" will become "Chirag Kukreja"
print(name.title())

#5. count() method will count the characters in the string and one has to give arguments in count to count a particular character

print(name.count("j"))